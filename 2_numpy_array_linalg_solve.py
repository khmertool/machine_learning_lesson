# numpy.linalg.solve

import numpy as np

'''
សត្វមួយក្រុម មានឆ្កែ និងមាន់ បូកសរុបឃើញ 36 ក្បាល
តែបើរាប់ជើងវិញ ឃើងចំនួន 100
ឆ្កែ = x
មាន់ = y
ជើងឆ្កែ = 4x
ជើងមាន់ = 2y

ប្រព័ន្ធសមីការ      មាទ្រិច
x + y = 36      1 1 | 36
4x + 2y = 100   4 2 | 100
'''
a = np.array([[1,1], [4,2]])
b = np.array([36,100])

result = np.linalg.solve(a, b)

print(result) #[14. 22.]

'''
 ឆ្កែ = 14
 មាន់ = 22
'''